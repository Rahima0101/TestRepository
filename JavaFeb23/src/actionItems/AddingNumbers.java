package actionItems;

public class AddingNumbers {

	public static void main(String[] args) {
		// write a program that sums the numbers from 0 to 15 using for, while and do while
		
		int sum = 0;
		for(int i = 0; i<=15; i++)
		{
			sum = sum + i;
		}
         System.out.println(" The result is: " + sum);
	}

}
